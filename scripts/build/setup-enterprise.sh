}

# Main setup flow
main() {
    echo -e "${BLUE}ğŸš€ Starting enterprise setup...${NC}"
    echo ""
    
    # Check if .env already exists
    if [ -f "$PROJECT_ROOT/.env" ]; then
        if prompt_yes_no "âš ï¸  .env file already exists. Overwrite?" "n"; then
            create_env_file
        else
            echo -e "${YELLOW}ğŸ“ Using existing .env file${NC}"
        fi
    else
        create_env_file
    fi
    
    echo ""
    
    # Install dependencies if needed
    if [ ! -d "$PROJECT_ROOT/node_modules" ]; then
        install_dependencies
    else
        if prompt_yes_no "ğŸ“¦ Reinstall dependencies?" "n"; then
            install_dependencies
        fi
    fi
    
    echo ""
    
    # Build project
    if [ ! -d "$PROJECT_ROOT/dist" ]; then
        build_project
    else
        if prompt_yes_no "ğŸ”¨ Rebuild project?" "y"; then
            build_project
        fi
    fi
    
    echo ""
    
    # Optional health check
    if prompt_yes_no "ğŸ¥ Run health check?" "y"; then
        run_health_check
    fi
    
    echo ""
    show_usage_instructions
}

# Run setup if script is executed directly
if [[ "${BASH_SOURCE[0]}" == "${0}" ]]; then
    main "$@"
fi
